package loop;

public class Star {

  public static void main(String[] args) {
    // *
    for (int j = 5; j <= 5; j++) {
      System.out.print("*");
    }
    System.out.println();
    // **
    for (int j = 4; j <= 4; j++) {
      System.out.print("*");
    }
    System.out.println();
    // ***
    for (int j = 3; j <= 3; j++) {
      System.out.print("*");
    }
    System.out.println();
    // ****
    for (int j = 2; j <= 2; j++) {
      System.out.print("*");
    }
    System.out.println();
    // *****
    for (int i = 1; i < 1; i++) {
      System.out.print("*");
    }
    System.out.println();
  }
}
